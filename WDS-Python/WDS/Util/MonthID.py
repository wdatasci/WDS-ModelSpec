def CleanDate(v,tv=None,isDateEpochExcel=False,day=1,toWARN=True):
    if tv is None: 
        tv=type(v)
    if tv is datetime.date: return v
    if tv is datetime.datetime: return v.date()
    try:
        if tv in (int,float):
            # if value is comming in as YYYYMM or YYYYMMDD
            if 190000<v and v<210000:
                y=int(v/100)
                m=int(v-y*100)
                if day>0:
                    return datetime.date(y,m,day)
                else:
                    if m<12:
                        return datetime.date(y,m+1,1)+datetime.timedelta(day)
                    else:
                        return datetime.date(y+1,1,1)+datetime.timedelta(day)
            elif 19000000<v and v<21000000:
                y=int(v/10000)
                m=int((v-y*10000)/100)
                m=int(v-y*10000-m*100)
                return datetime.date(y+1,1,1)+datetime.timedelta(day)
            elif -1200<v and v<1213:  # a MonthID
                y=math.floor((lv-1)/12)
                m=int(lv-12*y)
                y+=2000
                if day>0:
                    lv=datetime.date(y,m,day)
                else:
                    if m<12:
                        lv=datetime.date(y,m+1,1)+datetime.timedelta(day)
                    else:
                        lv=datetime.date(y+1,1,1)+datetime.timedelta(day)
                if toWARN: print('Friendly Warning, assumed '+str(v)+' was a MonthID in CleanDate')
                return lv
            elif isDateEpochExcel:
                lv=DateEpochExcel+datetime.timedelta(int(v),0,0)
                if (lv.year>2040) and toWARN:
                    print('WARNING, in CleanDate, with isDateEpochExcel, converted int/float to a year>2040, might not be Excel Date')
                return lv
            else:
                lv=DateEpoch+datetime.timedelta(int(v),0,0)
                if (lv.year>2040) and toWARN:
                    print('WARNING, in CleanDate, with isDateEpochExcel=False, converted int/float to a year>2040, might be Excel Date')
                return lv
        elif tv in (str,bytes):
            lv=v if tv is str else v.decode()
            for f in (DateFormatRE1, DateFormatRE3, DateFormatRE2):
                prs=f.findall(lv)
                if (len(prs)==1) and (len(prs[0])>=5) and (prs[0][1]==prs[0][3]): 
                    lv=datetime.date(int(prs[0][0]),int(prs[0][2]),int(prs[0][4]))
                    return lv
            #check the yyyy-mm format
            prs=DateFormatRE1_2.findall(lv)
            if (len(prs)==1) and (len(prs[0])==3): 
                if day>0:
                    lv=datetime.date(int(prs[0][0]),int(prs[0][2]),day)
                else:
                    m=int(prs[0][2])
                    if m<12:
                        lv=datetime.date(int(prs[0][0]),m+1,1)+datetime.timedelta(day)
                    else:
                        lv=datetime.date(int(prs[0][0])+1,1,1)+datetime.timedelta(day)
                return lv
            #check the m?m-d?d-yy2yy format
            prs=DateFormatRE2_2.findall(lv)
            if (len(prs)==1) and (len(prs[0])==6) and (prs[0][1]==prs[0][3]): 
                prs[0]=list(prs[0])
                prs[0][0]=int(prs[0][0])
                prs[0][5]=int(prs[0][5])
                if prs[0][4]=='':
                    if prs[0][5]<Y2KThreshold:
                        prs[0][4]=2000
                    else:
                        prs[0][4]=1900
                else:
                    prs[0][4]=int(prs[0][4])
                lv=datetime.date(prs[0][4]+prs[0][5],prs[0][0],int(prs[0][2]))
                return lv
            #check the m?m-d?d-yy2yy 00:00:00 format
            prs=DateFormatRE3_2.findall(lv)
            if (len(prs)==1) and (len(prs[0])==6) and (prs[0][1]==prs[0][3]): 
                prs[0]=list(prs[0])
                prs[0][0]=int(prs[0][0])
                prs[0][5]=int(prs[0][5])
                if prs[0][4]=='':
                    if prs[0][5]<Y2KThreshold:
                        prs[0][4]=2000
                    else:
                        prs[0][4]=1900
                else:
                    prs[0][4]=int(prs[0][4])
                lv=datetime.date(prs[0][4]+prs[0][5],prs[0][0],int(prs[0][2]))
                return lv
        raise Exception('cannot convert '+str(v))
    except Exception as e:
        raise Exception('Error in CleanDate: '+e.args[0]+'\n'+str(traceback.format_tb(e.__traceback__)))


def Date2isoformat(v):
    if type(v) is datetime.date:
        return v.isoformat()
    if type(v) is datetime.datetime:
        return v.isoformat()
    return None

def MonthID(v):
    try:
        lv=CleanDate(v)
        return (lv.year-2000)*12+lv.month
    except Exception as e:
        raise e

def MonthID2Date(v,day=1):
    try:
        tv=type(v)
        if tv not in (int, float):
            lv=int(v)
        else:
            lv=v
        y=math.floor((lv-1)/12)
        m=int(lv-12*y)
        y+=2000
        if day>0:
            return datetime.date(y,m,day)
        else:
            if m<12:
                return datetime.date(y,m+1,1)+datetime.timedelta(day)
            else:
                return datetime.date(y+1,1,1)+datetime.timedelta(day)
    except:
        raise Exception('Cannot convert '+str(v)+' (with day='+str(day)+') in MonthID2Date')


